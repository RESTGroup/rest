impl C2S {
    pub fn to_matrixfullslice(&self) -> MatrixFullSlice<f64> {
        match &self {
            C2S::L0(matr) => {
                MatrixFullSlice {
                    size: &matr.size,
                    indicing: &matr.indicing,
                    data: &matr.data
                }
            },
            C2S::L1(matr) => {
                MatrixFullSlice {
                    size: &matr.size,
                    indicing: &matr.indicing,
                    data: &matr.data
                }
            },
            C2S::L2(matr) => {
                MatrixFullSlice {
                    size: &matr.size,
                    indicing: &matr.indicing,
                    data: &matr.data
                }
            },
            C2S::L3(matr) => {
                MatrixFullSlice {
                    size: &matr.size,
                    indicing: &matr.indicing,
                    data: &matr.data
                }
            },
            C2S::L4(matr) => {
                MatrixFullSlice {
                    size: &matr.size,
                    indicing: &matr.indicing,
                    data: &matr.data
                }
            },
        }
    }
}

pub fn c2s_matrix_const(l:usize) -> crate::constants::c2s::C2S {
    if l == 0 {
        C2S_L0
    } else if l ==1 {
        C2S_L1
    } else if l== 2 {
        C2S_L2
    } else if l== 3 {
        C2S_L3
    } else if l== 4 {
        C2S_L4
    } else {
        panic!("No C2S transformation implementation for l > 4")
    }
}

